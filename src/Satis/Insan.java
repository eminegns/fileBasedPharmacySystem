package Satis;

public class Insan {
    private String isim;
    private String soyisim;

    public Insan(String isim, String soyisim) {
        this.isim = isim;
        this.soyisim = soyisim;
    }

    public String getIsim() {
        return isim;
    }

    public String getSoyisim() {
        return soyisim;
    }

}